const initial = {
    prdsHome : productHome,
    
    prdsMale : productMale,
    
    prdsFeMale : productFemale,

    isDisplayCart : false,
    isDisplaySearch : false,
    isDisplayFixCart: false  ,
    
    cart : [
  
    ]
}

export const Reducer = (state = initial , action)=>{
let {cart} = state;

switch( action.type ){
    case "toggle":
        return{
            ...state,
            isDisplayCart : action.status
        }
    case "search":
        return{
            ...state,
            isDisplaySearch : action.status
        }
        case "addToCart" : 
        let product = action.product
        let prod = cart.find(x=> x.id == product.id);
        if(prod){
            prod.quantity++;
        }
        else{
            product.quantity = 1 ;
            cart.push(product)
        }
        return{
            ...state,
            isDisplayCart : true,
            cart:[...cart]
        }
    case "deleteItems" :
        let newCart = state.cart.filter(x=>x.id != action.id);
        return{
            ...state,
            cart : [...newCart]
        }

    case "increace" :
        let itemincreace = action.prod;
        let isItem = cart.find(x=>x.id == itemincreace.id);
        if(isItem){
            isItem.quantity++
        }
        return{
            ...state,
            cart:[...cart]
        }
    case "fixToCart" :
        return{
            ...state,
            isDisplayFixCart : action.status
        }

    case "decrease" :
        let itemdecreace = action.prod;
        let isitem = cart.find(x=>x.id == itemdecreace.id);
        if(isitem && isitem.quantity>0 ){
            isitem.quantity--
        }
        return{
            ...state,
            cart:[...cart]
        }
        
    default :
        return state
    }

}
